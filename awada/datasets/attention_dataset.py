import os
import random

import numpy as np
import torch
import torchvision.transforms.functional as TF
from PIL import Image
from torch.utils.data import Dataset


class AttentionPairedDataset(Dataset):
    """
    Dataset for AWADA GAN training.
    Yields random 128x128 patches from source images + corresponding source attention patches,
    plus random 128x128 patches from target images + corresponding target attention patches.

    Source attention maps are generated by a detector trained on the source domain.
    Target attention maps are generated by a detector trained on CyCada-stylized images
    (source images translated to the target style), applied to target domain images.
    When no target attention maps are provided, uniform (all-ones) weights are used.
    """

    def __init__(
        self,
        source_root: str,
        target_root: str,
        source_attention_root: str,
        target_attention_root: str | None = None,
        patch_size: int = 128,
    ) -> None:
        """Initialise the dataset.

        Args:
            source_root: Directory containing source-domain images.
            target_root: Directory containing target-domain images.
            source_attention_root: Directory containing source attention maps
                (``.npy`` files generated by ``generate_attention_maps.py``).
            target_attention_root: Optional directory containing target attention
                maps.  When ``None``, uniform (all-ones) weights are used for
                the target domain.
            patch_size: Side length of the square patches sampled during
                training (default: 128).
        """
        if not os.path.isdir(source_root):
            raise FileNotFoundError(
                f"source_root directory not found: '{source_root}'. "
                "Please ensure the source domain images are present before constructing the dataset."
            )
        if not os.path.isdir(target_root):
            raise FileNotFoundError(
                f"target_root directory not found: '{target_root}'. "
                "Please ensure the target domain images are present before constructing the dataset."
            )
        if not os.path.isdir(source_attention_root):
            raise FileNotFoundError(
                f"source_attention_root (attention_root) directory not found: '{source_attention_root}'. "
                "Please run 'generate_attention_maps.py' to produce the required attention maps "
                "before constructing the dataset."
            )
        if target_attention_root is not None and not os.path.isdir(target_attention_root):
            raise FileNotFoundError(
                f"target_attention_root directory not found: '{target_attention_root}'. "
                "Please run 'generate_attention_maps.py' to produce the required target attention "
                "maps before constructing the dataset."
            )
        self.patch_size = patch_size
        self.source_files = sorted(
            [
                os.path.join(source_root, f)
                for f in os.listdir(source_root)
                if f.lower().endswith((".png", ".jpg", ".jpeg"))
            ]
        )
        self.target_files = sorted(
            [
                os.path.join(target_root, f)
                for f in os.listdir(target_root)
                if f.lower().endswith((".png", ".jpg", ".jpeg"))
            ]
        )
        self.source_attention_root = source_attention_root
        self.target_attention_root = target_attention_root

    def __len__(self) -> int:
        """Return the number of source images in the dataset."""
        return len(self.source_files)

    def _load_attention(self, img_path: str, attention_root: str) -> np.ndarray:
        """Load a ``.npy`` attention map for the given image.

        Attention maps are stored on disk as uint8 arrays to reduce disk
        usage.  This method casts the loaded data back to float32 before
        returning it.

        Args:
            img_path: Path to the image file whose attention map is requested.
            attention_root: Directory where ``.npy`` attention maps are stored.

        Returns:
            Float32 array of shape ``[H, W]``.

        Raises:
            FileNotFoundError: If the expected ``.npy`` attention map file is
                not found in ``attention_root``.  This indicates a
                misconfigured dataset; run ``generate_attention_maps.py`` to
                produce the missing file before restarting.
        """
        filename_stem = os.path.splitext(os.path.basename(img_path))[0]
        npy_path = os.path.join(attention_root, filename_stem + ".npy")
        if not os.path.exists(npy_path):
            raise FileNotFoundError(
                f"Attention map not found: '{npy_path}'. "
                "Please run 'generate_attention_maps.py' to produce the required "
                "attention maps before restarting the job."
            )
        return np.load(npy_path).astype(np.float32)  # type: ignore[no-any-return]

    def _crop_attention(
        self, img_path: str, attention_root: str, x: int, y: int, p: int
    ) -> torch.Tensor:
        """Load an attention map and crop a patch at position (x, y) with size p×p.

        Args:
            img_path: Path to the image whose attention map should be loaded.
            attention_root: Directory containing ``.npy`` attention maps.
            x: Left edge of the crop in pixels.
            y: Top edge of the crop in pixels.
            p: Side length of the square crop in pixels.

        Returns:
            Attention patch tensor of shape ``[1, p, p]``.

        Raises:
            FileNotFoundError: If the expected ``.npy`` attention map file is
                not found (propagated from :meth:`_load_attention`).
        """
        attention = self._load_attention(img_path, attention_root)
        att_patch = attention[y : y + p, x : x + p]
        # Pad if near border
        pad_h = max(0, p - att_patch.shape[0])
        pad_w = max(0, p - att_patch.shape[1])
        if pad_h > 0 or pad_w > 0:
            att_patch = np.pad(att_patch, ((0, pad_h), (0, pad_w)), mode="constant")
        return torch.from_numpy(att_patch).unsqueeze(0)  # [1, p, p]

    def __getitem__(
        self, idx: int
    ) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
        """Return a randomly cropped source patch, target patch, and their attention maps.

        Performs random cropping and random horizontal flipping, applied
        consistently to each image and its corresponding attention map.  Source
        and target crops are sampled independently.  Images are normalised to
        ``[-1, 1]``; attention patches retain their original ``[0, 1]`` values.

        Args:
            idx: Index into the source file list.

        Returns:
            Tuple of ``(source_patch, target_patch, source_att_patch, target_att_patch)``
            where each tensor has shape ``[C, patch_size, patch_size]`` for images
            (``C=3``) and ``[1, patch_size, patch_size]`` for attention maps.
        """
        src_path = self.source_files[idx]
        tgt_path = self.target_files[idx % len(self.target_files)]
        p = self.patch_size

        src_img = Image.open(src_path).convert("RGB")
        tgt_img = Image.open(tgt_path).convert("RGB")

        src_w, src_h = src_img.size
        tgt_w, tgt_h = tgt_img.size

        # Random crop source + source attention at same location
        src_x = random.randint(0, max(0, src_w - p))
        src_y = random.randint(0, max(0, src_h - p))
        src_patch = TF.to_tensor(src_img.crop((src_x, src_y, src_x + p, src_y + p)))
        source_att_patch = self._crop_attention(
            src_path, self.source_attention_root, src_x, src_y, p
        )

        # Random horizontal flip (applied consistently to image and attention map)
        if random.random() > 0.5:
            src_patch = TF.hflip(src_patch)
            source_att_patch = TF.hflip(source_att_patch)

        # Random crop target + target attention at same location
        tgt_x = random.randint(0, max(0, tgt_w - p))
        tgt_y = random.randint(0, max(0, tgt_h - p))
        tgt_patch = TF.to_tensor(tgt_img.crop((tgt_x, tgt_y, tgt_x + p, tgt_y + p)))
        if self.target_attention_root is not None:
            target_att_patch = self._crop_attention(
                tgt_path, self.target_attention_root, tgt_x, tgt_y, p
            )
        else:
            target_att_patch = torch.ones(1, p, p)

        # Random horizontal flip (applied consistently to image and attention map)
        if random.random() > 0.5:
            tgt_patch = TF.hflip(tgt_patch)
            target_att_patch = TF.hflip(target_att_patch)

        # Normalize to [-1, 1]
        src_patch = src_patch * 2 - 1
        tgt_patch = tgt_patch * 2 - 1

        return src_patch, tgt_patch, source_att_patch, target_att_patch
